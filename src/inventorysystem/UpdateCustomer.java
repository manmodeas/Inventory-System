    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String[] Customer = new String[8];
        Customer[0] = TFCustomerName.getText();
        long temp = 0;
        try {
            temp = Long.parseLong(TFMobileNo.getText());
            Customer[1] = String.valueOf(temp);
        } catch (NumberFormatException numberFormatException) {
            Customer[1] = "";
        }
        
        Customer[2] = TFEmail.getText();
        Customer[3] = TFAddress.getText();
        Customer[4] = TFGSTINNo.getText();
        Customer[5] = TFAadharNo.getText();
        Customer[6] = CustomerID;
        double PreDue = Double.valueOf(TFPreDue.getText());
        double Due = Double.valueOf(TFDue.getText());
        double RDue = PreDue - Due;
        if(Due <= PreDue && Due >= 0)
        {
            Customer[7] = String.valueOf(RDue);
            for(int i=0;i<Customer.length;i++)
            {
                Customer[i].trim();
            }

            if(Customer[0] == null || Customer[0].equals(""))
            {
                LCustomerName.setForeground(Color.red);
            }else if(Customer[1].length() != 10)
            {
                LCustomerName.setForeground(Color.WHITE);
                LMobileNo.setForeground(Color.red);
            }else if(Customer[2] == null || Customer[2].equals(""))
            {
                LCustomerName.setForeground(Color.WHITE);
                LMobileNo.setForeground(Color.WHITE);
                LEmail.setForeground(Color.red);
            }else if(Customer[3] == null || Customer[3].equals(""))
            {
                LCustomerName.setForeground(Color.WHITE);
                LMobileNo.setForeground(Color.WHITE);
                LEmail.setForeground(Color.WHITE);
                LAddress.setForeground(Color.red);
            }else if(Customer[4].length() != 15)
            {
                LCustomerName.setForeground(Color.WHITE);
                LMobileNo.setForeground(Color.WHITE);
                LEmail.setForeground(Color.WHITE);
                LAddress.setForeground(Color.WHITE);
                LGSTINNo.setForeground(Color.red);
            }else if(Customer[5].length() != 12)
            {
                LCustomerName.setForeground(Color.WHITE);
                LMobileNo.setForeground(Color.WHITE);
                LEmail.setForeground(Color.WHITE);
                LAddress.setForeground(Color.WHITE);
                LGSTINNo.setForeground(Color.WHITE);
                LAadharNo.setForeground(Color.red);
            }else if(TFDue.getText() == null || TFDue.getText().equals("")){
                LCustomerName.setForeground(Color.WHITE);
                LMobileNo.setForeground(Color.WHITE);
                LEmail.setForeground(Color.WHITE);
                LAddress.setForeground(Color.WHITE);
                LGSTINNo.setForeground(Color.WHITE);
                LAadharNo.setForeground(Color.WHITE);
                LDue.setForeground(Color.red);
            }else{
                LCustomerName.setForeground(Color.WHITE);
                LMobileNo.setForeground(Color.WHITE);
                LEmail.setForeground(Color.WHITE);
                LAddress.setForeground(Color.WHITE);
                LGSTINNo.setForeground(Color.WHITE);
                LAadharNo.setForeground(Color.WHITE);
                LDue.setForeground(Color.WHITE);
                try {
                    db.CustomerUpdate(Customer);
                    String[] TransactionD = new String[4];
                    TransactionD[0] = Customer[0];
                    TransactionD[1] = "Customer";
                    TransactionD[2] = "Taken";
                    TransactionD[3] = TFDue.getText();
                    db.TransactionTable(TransactionD);
                    JOptionPane.showMessageDialog(null, "Customer Details Updated Successfully");
                    this.DB.VCustomerUCount = 0;
                    UCM.setVisible(false);
                    if (WPanel.equals("ViewCustomer")) {
                        this.DB.VCCheckBoxandtableRefresh();
                    }else{
                        this.DB.PRCheckBoxandtableRefresh();
                    }
                    this.dispose();

                } catch (SQLException ex) {
                    Logger.getLogger(Supplier.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }else{
            LDue.setForeground(Color.red);
            JOptionPane.showMessageDialog(null, "Entered Value is more then PreDue");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

