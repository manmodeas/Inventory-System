    private void TFCustomerNameKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TFCustomerNameKeyReleased
        // TODO add your handling code here:
        String name = TFCustomerName.getText();
        boolean valid = (name != null) && pattern.matcher(name).matches();
        if(!valid)
        {
            try {
                name = name.substring(0, name.length() - 1);
            } catch (Exception e) {
            }
            TFCustomerName.setText(name);
        }
    }//GEN-LAST:event_TFCustomerNameKeyReleased

