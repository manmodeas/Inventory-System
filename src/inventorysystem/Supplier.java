    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String[] Supplier = new String[6];
        Supplier[0] = TFSupplierName.getText();
        long temp = 0;
        try {
            temp = Long.parseLong(TFMobileNo.getText());
            Supplier[1] = String.valueOf(temp);
        } catch (NumberFormatException numberFormatException) {
            Supplier[1] = "";
        }
        
        Supplier[2] = TFEmail.getText();
        Supplier[3] = TFAddress.getText();
        Supplier[4] = TFGSTINNo.getText();
        Supplier[5] = TFAadharNo.getText();
        
        for(int i=0;i<Supplier.length;i++)
        {
            Supplier[i].trim();
        }
            
        if(Supplier[0] == null || Supplier[0].equals(""))
        {
            LSupplierName.setForeground(Color.red);
        }else if(Supplier[1].length() != 10)
        {
            LSupplierName.setForeground(Color.WHITE);
            LMobileNo.setForeground(Color.red);
        }else if(Supplier[2] == null || Supplier[2].equals(""))
        {
            LSupplierName.setForeground(Color.WHITE);
            LMobileNo.setForeground(Color.WHITE);
            LEmail.setForeground(Color.red);
        }else if(Supplier[3] == null || Supplier[3].equals(""))
        {
            LSupplierName.setForeground(Color.WHITE);
            LMobileNo.setForeground(Color.WHITE);
            LEmail.setForeground(Color.WHITE);
            LAddress.setForeground(Color.red);
        }else if(Supplier[4].length() != 15)
        {
            LSupplierName.setForeground(Color.WHITE);
            LMobileNo.setForeground(Color.WHITE);
            LEmail.setForeground(Color.WHITE);
            LAddress.setForeground(Color.WHITE);
            LGSTINNo.setForeground(Color.red);
        }else if(Supplier[5].length() != 12)
        {
            LSupplierName.setForeground(Color.WHITE);
            LMobileNo.setForeground(Color.WHITE);
            LEmail.setForeground(Color.WHITE);
            LAddress.setForeground(Color.WHITE);
            LGSTINNo.setForeground(Color.WHITE);
            LAadharNo.setForeground(Color.red);
        }else{
            LSupplierName.setForeground(Color.WHITE);
            LMobileNo.setForeground(Color.WHITE);
            LEmail.setForeground(Color.WHITE);
            LAddress.setForeground(Color.WHITE);
            LGSTINNo.setForeground(Color.WHITE);
            LAadharNo.setForeground(Color.WHITE);
            try {
                db.Supplier(Supplier);
                TFSupplierName.setText("");
                TFMobileNo.setText("");
                TFEmail.setText("");
                TFAddress.setText("");
                TFGSTINNo.setText("");
                TFAadharNo.setText("");
                
            } catch (SQLException ex) {
//                Logger.getLogger(Supplier.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        DB.setSupplierName();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void TFSupplierNameKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TFSupplierNameKeyReleased
        // TODO add your handling code here:
        String name = TFSupplierName.getText();
        boolean valid = (name != null) && pattern.matcher(name).matches();
        if(!valid)
        {
            try {
                name = name.substring(0, name.length() - 1);
            } catch (Exception e) {
            }
            TFSupplierName.setText(name);
        }
        
    }//GEN-LAST:event_TFSupplierNameKeyReleased

